-- Golf Charity Subscription Platform schema for Supabase

-- 1) profiles
create table profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  handicap integer,
  charity_id bigint,
  charity_pct integer not null default 10 check (charity_pct >= 10 and charity_pct <= 100),
  subscription_status text not null default 'inactive',
  created_at timestamptz not null default now()
);

-- 2) charities
create table charities (
  id bigint generated by default as identity primary key,
  name text not null,
  description text,
  image_url text,
  featured boolean not null default false,
  created_at timestamptz not null default now()
);

-- Add FK from profiles.charity_id -> charities.id after both tables exist
alter table profiles
  add constraint profiles_charity_id_fkey
  foreign key (charity_id)
  references charities(id)
  on delete set null;

-- 3) scores
create table scores (
  id bigint generated by default as identity primary key,
  user_id uuid not null references profiles(id) on delete cascade,
  score integer not null check (score >= 1 and score <= 45),
  played_at date not null,
  created_at timestamptz not null default now()
);

-- 4) subscriptions
create table subscriptions (
  id bigint generated by default as identity primary key,
  user_id uuid not null references profiles(id) on delete cascade,
  stripe_subscription_id text,
  stripe_customer_id text,
  plan text not null check (plan in ('monthly', 'yearly')),
  status text not null default 'inactive',
  current_period_end timestamptz,
  created_at timestamptz not null default now()
);

-- 5) draws
create table draws (
  id bigint generated by default as identity primary key,
  month date not null,
  numbers integer[] not null check (cardinality(numbers) = 5),
  status text not null default 'pending' check (status in ('pending', 'simulated', 'published')),
  draw_mode text not null default 'random' check (draw_mode in ('random', 'algorithmic')),
  jackpot_amount numeric(10,2) not null default 0,
  jackpot_carried_over boolean not null default false,
  created_at timestamptz not null default now()
);

-- 6) draw_results
create table draw_results (
  id bigint generated by default as identity primary key,
  draw_id bigint not null references draws(id) on delete cascade,
  user_id uuid not null references profiles(id) on delete cascade,
  match_type integer not null check (match_type in (3, 4, 5)),
  tier_pool_amount numeric(10,2) not null default 0,
  winners_count integer not null default 1,
  individual_share numeric(10,2) not null default 0,
  proof_url text,
  payment_status text not null default 'pending' check (payment_status in ('pending', 'approved', 'rejected', 'paid')),
  created_at timestamptz not null default now()
);

-- 7) donations
create table donations (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id) on delete set null,
  charity_id bigint not null references charities(id) on delete cascade,
  amount numeric(10,2) not null,
  stripe_payment_id text,
  created_at timestamptz not null default now()
);

