-- Trigger function: enforce max 5 scores per user
create or replace function limit_scores_per_user()
returns trigger
language plpgsql
as $$
begin
  if (select count(*) from scores where user_id = NEW.user_id) >= 5 then
    delete from scores
    where id = (
      select id from scores
      where user_id = NEW.user_id
      order by created_at asc
      limit 1
    );
  end if;
  return NEW;
end;
$$;

create trigger enforce_score_limit
  before insert on scores
  for each row
  execute function limit_scores_per_user();

